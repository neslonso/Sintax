					<div>
						<p>Las cookies son breves informaciones que se envían y almacenan en el disco duro del ordenador del usuario a través de su navegador cuando éste se conecta a una web. Las cookies se pueden utilizar para recabar y almacenar datos del usuario mientras está conectado para facilitarle los servicios solicitados y que no se suelen conservar (cookies de sesión), o para conservar los datos del usuario para otro tipo de servicios futuros y que se pueden conservar por tiempo indefinido (cookies persistentes). Las cookies pueden ser propias o de terceros.</p></p>
						<br><p>Existen varios tipos de cookies:</p>
						<br><p>Cookies técnicas que facilitan la navegación del usuario y la utilización de las diferentes opciones o servicios que ofrece la web como identificar la sesión, permitir el acceso a determinadas áreas, facilitar pedidos, compras, cumplimentación de formularios, inscripciones, seguridad, facilitar funcionalidades (vídeos, redes sociales, etc.).</p>
						<br><p>Cookies de personalización que permiten al usuario acceder a los servicios según sus preferencias (idioma, navegador, configuración, etc.).</p>
						<br><p>Cookies de análisis que permiten el análisis anónimo del comportamiento de los usuarios de la web y que permiten medir la actividad del usuario y elaborar perfiles de navegación con el fin objetivo de mejorar los sitios web.</p>
						<br><p>Cookies publicitarias que permiten la gestión de los espacios publicitarios de la web.</p>
						<br><p>Cookies de publicidad personalizada que permiten la gestión de los espacios publicitarios de la web en base al comportamiento y hábitos de navegación del usuario, de donde se obtiene su perfil y este hecho permite personalizar la publicidad que se muestra en el navegador del usuario.</p>
